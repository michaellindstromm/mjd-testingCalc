function add (one, two) {
  return one + two;
};

function subtract (one, two) {
  return one - two;
};

function multiply (one, two) {
  return one * two;
};

function divide (one, two) {
  return one / two;
};

function square (one) {
  return one*one;
};

function squareRoot (one) {
  return Math.sqrt(one);
};
